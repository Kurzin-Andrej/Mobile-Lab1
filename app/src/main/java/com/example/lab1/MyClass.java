package com.example.lab1;

public class MyClass {

    public static int Min( int a, int b ){
        if( a < b ){
            return a;
        } else {
            return b;
        }
    }

    public static int Max( int a, int b ){
        if( a > b ){
            return a;
        } else {
            return b;
        }
    }

}
